"""Safe copy-ready text builders for the Telegram engineer mode."""

from __future__ import annotations

import re

DEFAULT_ENGINEER_TASK = "уточнить задачу"
SECRET_VALUE_PATTERN = re.compile(
    r"(?i)\b(bot_token|deepseek_api_key|openai_api_key|api_key|token|password|"
    r"токен|пароль|ключ)\s*[:=]\s*\S+"
)


def sanitize_engineer_task_text(user_task_text: str | None) -> str:
    """Normalize a user task without exposing or preserving secret-like requests."""

    text = " ".join(str(user_task_text or "").split())
    if not text:
        return DEFAULT_ENGINEER_TASK

    return SECRET_VALUE_PATTERN.sub(r"\1=<redacted>", text)


